package ejer_3_operaciones_relacionales;

public class ejer_3 {

	public static void main(String[] args) {
		
		int a = 4; /*el int establece la variable, es para declararla, solo para enteros*/
		int b = 3;
		
		System.out.println(a == b);
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		
		System.out.println("");
		
		a = 14;
		b = -2;
		
		System.out.println(a == b);
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		

	}

}
