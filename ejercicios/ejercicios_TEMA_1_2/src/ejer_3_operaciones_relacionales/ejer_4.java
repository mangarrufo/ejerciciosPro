package ejer_3_operaciones_relacionales;

public class ejer_4 {

	public static void main(String[] args) {
		
		float a = 2.3F;
		float b = 4.9F;
		
		System.out.println(a == b);
		System.out.println(a > b);
		System.out.println(a < b);
		System.out.println(a >= b);
		System.out.println(a <= b);
			

	}

}
